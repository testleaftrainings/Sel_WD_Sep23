package week1.day2;

public class LearnDataTypes {
	
	public static void main(String[] args) {
		
		//datatype varName = value;
		
			short  rollNo  = 12;
			int 	age    =  29;
			long phonNumber = 9876543210L;
			//single quotes
			char  initial  = 'R';
			//double quotes
			//Non primitive datatype - String is a Class
			String  name   = "Ranjini";
			//boolean can hold either true or false
			boolean breakfast = true;	
			float  height = 165.5f;	
			double accountBalance = 15000.7875367;
			
			System.out.println("My roll number" +"=" +rollNo);
			System.out.println(age);
			System.out.println(phonNumber);
			System.out.println(initial+" " + name);
		//	System.out.println(name);
			System.out.println(breakfast);
			System.out.println(height);
			System.out.println(accountBalance);
			
			
		
	}

}
